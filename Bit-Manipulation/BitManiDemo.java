import java.util.*;

class BitManiDemo {
    public static void main(String[] args) {
        System.out.println("Binary AND operation of 5 & 6 : "+ (5 & 6));
        System.out.println("Binary OR operation of 5 & 6 : "+ (5 | 6));
        System.out.println("Binary XOR operation of 5 & 6 : "+ (5 ^ 6));
        System.out.println("Binary NOT operation of 5 : "+ (~5));
        

    }
}